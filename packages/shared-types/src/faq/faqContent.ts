/**
 * Flatten all FAQ items across categories for search
 */
export function getAllFaqItems(): (FaqItem & { categoryId: string; categoryTitle: string })[] {
  return faqCategories.flatMap(cat =>
    cat.items.map(item => ({
      ...item,
      categoryId: cat.id,
      categoryTitle: cat.title,
    }))
  );
}

/**
 * Search FAQ items by query string (matches question, answer, and keywords)
 */
export function searchFaqItems(
  query: string
): (FaqItem & { categoryId: string; categoryTitle: string })[] {
  const lowerQuery = query.toLowerCase().trim();
  if (!lowerQuery) return [];

  return getAllFaqItems().filter(
    item =>
      item.question.toLowerCase().includes(lowerQuery) ||
      item.answer.toLowerCase().includes(lowerQuery) ||
      item.keywords.some(kw => kw.toLowerCase().includes(lowerQuery))
  );
}
